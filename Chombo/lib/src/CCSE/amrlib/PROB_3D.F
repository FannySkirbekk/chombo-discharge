#ifdef BL_DO_FLCT
                     x = domnlo(1) + (i+0.5)*dx(1)
                     r = SQRT( x*x + y*y )
c                    call vswirlXYZ(x,y,u1,u2,u3)
                     if (r.gt.Rfu) then
                        u_inf = Vco
                        eta = TANH(2*(r-Rfu)/Rtran)
