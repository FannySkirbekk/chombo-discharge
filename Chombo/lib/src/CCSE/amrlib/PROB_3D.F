      REAL_T     vel    (DIMV(state) ,SDIM)
      REAL_T     rho    (DIMV(state))
      REAL_T     tracer (DIMV(state))
      REAL_T     gravity
      integer    comps(10)

#include "probdata_3D.H"

c
c     ::::: local variables
c
      integer i, j, k, n
      integer ilo, jlo, klo
      integer ihi, jhi, khi
      REAL_T  x, y, z
      REAL_T  hx, hy, hz
      REAL_T  r, r2

      hx = dx(1)
      hy = dx(2)
      hz = dx(3)

      ilo = istate_l1
      jlo = istate_l2
      klo = istate_l3
      ihi = istate_h1
      jhi = istate_h2
      khi = istate_h3

c      write(*,*) "Comps 1-5 are :",comps(1),comps(2),comps(3),comps(4),comps(5)

      do n = scomp, scomp+ncomp-1
         if (n.eq.comps(1)) then
c
c     Density
c
            do k = klo, khi
               do j = jlo, jhi
                  do i = ilo, ihi
                     force(i,j,k,n) = zero
                  enddo
               enddo
            enddo
         else if (n.eq.comps(2)) then
c
c     Tracer
c
            if (probtype.eq.99) then
c     Boussinesq
               do k = klo, khi
                  z = xlo(3) + hz*(float(k-klo) + half)
                  do j = jlo, jhi
                     y = xlo(2) + hy*(float(j-jlo) + half)
                     do i = ilo, ihi
                        x = xlo(1) + hx*(float(i-ilo) + half)

                        if (sqrt(x*x+y*y).lt.radblob) then
c     force(i,j,k,n) = heatflux
                           force(i,j,k,n) = zero
                        else
                           force(i,j,k,n) = zero
                        endif
                     enddo
                  enddo
               enddo
            else
c     Tracer - not probtype 99
               do k = klo, khi
                  do j = jlo, jhi
                     do i = ilo, ihi
                        force(i,j,k,n) = zero
                     enddo
                  enddo
               enddo
            endif
         else
c     Default to zero
            do k = klo, khi
               do j = jlo, jhi
                  do i = ilo, ihi
                     force(i,j,k,n) = zero
                  enddo
               enddo
            enddo
         endif
      enddo

      end


